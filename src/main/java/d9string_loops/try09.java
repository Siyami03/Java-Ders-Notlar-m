package d9string_loops;

import java.util.Scanner;

public class try09 {
    public static void main(String[] args) {

        //ornek 1: 6'dan 10'a kadar tum int'lerin toplamini console'a yazdiriniz
        //6+7+8+9+10=40 ==> 40
        int sum = 0;
        for (int i = 6; i < 11 ; i++) {
            sum = sum + i;

        }
        System.out.println(sum);

        int carpim = 1;
        for (int i = 6; i < 11 ; i++) {
            carpim = carpim * i;

        }
        System.out.println(carpim);









    }
}






























